// Функция для генерации вопросов в форме
function renderQuiz() {
    const quizForm = document.getElementById('quizForm');
    quizForm.innerHTML = '';  // очищаем форму перед генерацией

    questions.forEach((q, index) => {
        const questionDiv = document.createElement('div');
        questionDiv.className = 'question';
        
        const questionText = document.createElement('p');
        questionText.textContent = `${index + 1}. ${q.question}`;
        questionDiv.appendChild(questionText);

        // Генерация вариантов ответов
        q.answers.forEach((answer, i) => {
            const label = document.createElement('label');
            const radio = document.createElement('input');
            radio.type = 'radio';
            radio.name = `q${index}`;
            radio.value = i;
            label.appendChild(radio);
            label.appendChild(document.createTextNode(` ${i + 1}) ${answer}`));
            questionDiv.appendChild(label);
            questionDiv.appendChild(document.createElement('br'));
        });

        quizForm.appendChild(questionDiv);
    });
}

// Функция для проверки ответов
function checkAnswers() {
    let correctAnswers = 0;

    questions.forEach((q, index) => {
        const selectedAnswer = document.querySelector(`input[name="q${index}"]:checked`);
        if (selectedAnswer && parseInt(selectedAnswer.value) === q.correct) {
            correctAnswers++;
        }
    });

    // Вывод результата
    // Вывод результата
const resultDiv = document.getElementById('result');
const percentage = (correctAnswers / questions.length) * 100;
resultDiv.innerText = `Ты ответил правильно на ${correctAnswers} из ${questions.length} вопросов (${percentage.toFixed(2)}%).`;

}

// Генерация опроса при загрузке страницы
window.onload = function() {
    renderQuiz();
};
